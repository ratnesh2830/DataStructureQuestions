struct Node
{
    Node*leftchild;
    int data;
    Node*rightchild;
};

class Solution
{
    public:
    int MaxPathSum(Node *p,int &maxi)
    {
        if(p==NULL)
        return 0;

        int left=max(0,MaxPathSum(p->leftchild,maxi));
        int right=max(0,MaxPathSum(p->rightchild,maxi));

        maxi=max(maxi,p->data+left+right);
        
        return p->data+max(left,right);
    }

    int MaximumPathSum(Node *root)
    {
        int maxi=0;

        MaxPathSum(root,maxi);
    return maxi;
    }
};


